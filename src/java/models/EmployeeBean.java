package models;

import java.math.BigDecimal;
import java.sql.Date;

public class EmployeeBean {
    private int employeeId;            // INT(11) - Primary Key, AUTO_INCREMENT
    private String employeeName;       // VARCHAR(100)
    private String email;              // VARCHAR(100), Indexed
    private String phoneNumber;        // VARCHAR(15)
    private String position;           // VARCHAR(50)
    private BigDecimal salary;         // DECIMAL(10,2)
    private Date hireDate;             // DATE
    private java.sql.Timestamp createdAt; // TIMESTAMP

    // Getters and Setters
    public int getEmployeeId() {
        return employeeId;
    }

    public void setEmployeeId(int employeeId) {
        this.employeeId = employeeId;
    }

    public String getEmployeeName() {
        return employeeName;
    }

    public void setEmployeeName(String employeeName) {
        this.employeeName = employeeName;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getPhoneNumber() {
        return phoneNumber;
    }

    public void setPhoneNumber(String phoneNumber) {
        this.phoneNumber = phoneNumber;
    }

    public String getPosition() {
        return position;
    }

    public void setPosition(String position) {
        this.position = position;
    }

    public BigDecimal getSalary() {
        return salary;
    }

    public void setSalary(BigDecimal salary) {
        this.salary = salary;
    }

    public Date getHireDate() {
        return hireDate;
    }

    public void setHireDate(Date hireDate) {
        this.hireDate = hireDate;
    }

    public java.sql.Timestamp getCreatedAt() {
        return createdAt;
    }

    public void setCreatedAt(java.sql.Timestamp createdAt) {
        this.createdAt = createdAt;
    }
}
